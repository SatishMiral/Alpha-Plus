public class typeCasting {
    public static void main(String[] args) {
        float a = 25.12f;
        int b = (int) a;
        System.out.println(b);

        char ch = 'a';
        char ch2 = 'b';
        int num = ch;
        int num2 = ch2;
        System.out.println(num);
        System.out.println(num2);
    }
}
